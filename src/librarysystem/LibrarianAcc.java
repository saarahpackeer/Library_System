    private void addBookButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addBookButtonMouseClicked
        
        DefaultTableModel dm = (DefaultTableModel)table2.getModel();
        while(dm.getRowCount()>0) {
            for(int i=0; i<dm.getRowCount(); i++) {
            dm.removeRow(i);
            }
        }
        
        if(isbn.getText().equals("") || isbn.getText().equals("") || author.getText().equals("") || edition.getText().equals("") || pubdate.getText().equals("") || keywords.getText().equals("") || location.getText().equals("") || status.getText().equals("") ) {
            JOptionPane.showMessageDialog(new JFrame(), "Please enter data in all fields.");
        } else {
            try {
                b = new Book(isbn.getText(), title.getText(), author.getText(), edition.getText(), pubdate.getText(), keywords.getText(), location.getText(), status.getText());
                l.addBook(b, bookdatabase);        
                bookdatabase.clear();
                l.loadBooks(bookdatabase);    
                JOptionPane.showMessageDialog(new JFrame(), "You have successfully added a new Book record");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(new JFrame(), "Error:"+e);
            }
            for(Book b: bookdatabase) {
                String[] data = {b.getISBN(),b.getTitle(),b.getAuthor(),b.getEdition(),b.getPubdate(),b.getTerms(),b.getLocation(),b.getStatus()};
                dm.addRow(data);
            }            
        }
        
        
        
        
        
               
        
    }//GEN-LAST:event_addBookButtonMouseClicked

