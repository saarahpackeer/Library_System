    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        // TODO add your handling code here:
        
        if(isbn1.getText().equals("")) {
            JOptionPane.showMessageDialog(new JFrame(), "Please enter a valid ISBN.");
        } else {
            if(findBook.getText().chars().allMatch(Character::isDigit)) {
                bookdatabase.clear();
                l.loadBooks(bookdatabase);  
                Book found = null;
                try {
                String entered = isbn1.getText();
                for (Book bookk : bookdatabase) {
                    if (entered.equals(bookk.getISBN())) {
                        found = bookk;
                        title1.setText(found.getTitle());
                        author1.setText(found.getAuthor());
                        edition1.setText(found.getEdition());
                        pubdate1.setText(found.getPubdate());
                        keywords1.setText(found.getTerms());
                        location1.setText(found.getLocation());
                        status1.setText(found.getStatus());
                        JOptionPane.showMessageDialog(new JFrame(), "Book found!");
                    } 
                }
                if (!entered.equals(found.getISBN()))  {
                    JOptionPane.showMessageDialog(new JFrame(), "Book not found.");
                } 
                
                DefaultTableModel dm = (DefaultTableModel)table.getModel();
                String[] data = {found.getISBN(),found.getTitle(),found.getAuthor(),found.getEdition(),found.getPubdate(),found.getTerms(),found.getLocation(),found.getStatus()};
                dm.addRow(data);
                } catch (Exception e){
                    System.out.println("error: "+e);
                }
            }
        }
        
    }//GEN-LAST:event_jButton2MouseClicked

