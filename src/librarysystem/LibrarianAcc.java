    private void findBookBMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_findBookBMouseClicked
        // TODO add your handling code here:
        
        if(findBook.getText().equals("")) {
                JOptionPane.showMessageDialog(new JFrame(), "No ISBN entered.");
        } else {
            if(findBook.getText().chars().allMatch(Character::isDigit)) {
                bookdatabase.clear();
                l.loadBooks(bookdatabase);  
                Book found = null;
                try {
                    String entered = findBook.getText();
                    for (Book bookk : bookdatabase) {
                        if (entered.equals(bookk.getISBN())) {
                        found = bookk;
                        } 
                    }        
                    
                    DefaultTableModel dm = (DefaultTableModel)table.getModel();
                    String[] data = {found.getISBN(),found.getTitle(),found.getAuthor(),found.getEdition(),found.getPubdate(),found.getTerms(),found.getLocation(),found.getStatus()};
                    dm.addRow(data);
                } catch (Exception e){
                    System.out.println("error: "+e);
                    JOptionPane.showMessageDialog(new JFrame(), "ERROR: The book does not exist.");
                }
            } else {
                JOptionPane.showMessageDialog(new JFrame(), "The ISBN can only be digits.");
            }
        }      
        
        

        
    }//GEN-LAST:event_findBookBMouseClicked

    private void findBookB1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_findBookB1MouseClicked
        // TODO add your handling code here:
        
        if(findBook1.getText().equals("")) {
            JOptionPane.showMessageDialog(new JFrame(), "There is no ISBN entered.");
        } else {
            DefaultTableModel dm = (DefaultTableModel)table1.getModel();
            while(dm.getRowCount()>0) {
            for(int i=0; i<dm.getRowCount(); i++) {
            dm.removeRow(i);
        }
        } 
        
        
            List<Book> keywordsearch = new ArrayList<Book>(); 
            l.loadBooks(bookdatabase);  
            Book found = null;
            try {
                String enteredisbn = findBook1.getText();
        
                for (Book bookk : bookdatabase) {
                if (enteredisbn.equals(bookk.getISBN()) || enteredisbn.equals(bookk.getTitle()) || bookk.getTerms().contains(enteredisbn)) {
                    found = bookk;
                    keywordsearch.add(bookk);
            } 
        }

        for(int i=0; i<keywordsearch.size(); i++) {
            String[] data = {keywordsearch.get(i).getISBN(),keywordsearch.get(i).getTitle(),keywordsearch.get(i).getAuthor(),keywordsearch.get(i).getEdition(),keywordsearch.get(i).getPubdate(),keywordsearch.get(i).getTerms(),keywordsearch.get(i).getLocation(),keywordsearch.get(i).getStatus()};
            dm.addRow(data);
        }      
        } catch (Exception e){
            System.out.println("error: "+e);
        }
        }
        
        
    }//GEN-LAST:event_findBookB1MouseClicked

